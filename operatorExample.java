class operatorExample{
    public static void main(String args[]){
int a=10;
int b=-10;
int x=10;
int y=10;
boolean c=true;
boolean d=false;
System.out.println(x++ + ++x);
System.out.println(y++ + y++);
System.out.println(~a);
System.out.println(~b);
System.out.println(!c);
System.out.println(!d);

}
}